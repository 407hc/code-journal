# As Estruturas de Repetição (ou Loops) servem para executar o mesmo bloco de código várias vezes seguidas, sem que você precise reescrever as linhas.
# A primeira delas é o while (que significa "enquanto"). Ele repete um bloco de código enquanto uma condição que você definiu continuar sendo verdadeira.
contador = 1
while contador <= 3:
    print(contador)
    contador = contador + 1
